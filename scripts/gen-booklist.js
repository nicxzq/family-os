#!/usr/bin/env node
/**
 * Generate storybooks/booklist.js from the card order in for-youngest.html.
 *
 * The youngest child's storybooks are added by a daily scheduled task, so the
 * reading order must never be hand-maintained. for-youngest.html is the single
 * source of truth: cards appear there in shelf order.
 *
 * Only numeric books (NN-*.html) are included — the middle child's `m##` bear
 * series has its own shelf and is excluded.
 *
 * Chained from scripts/sync-gate.js. Run standalone: node scripts/gen-booklist.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SOURCE = path.join(ROOT, 'for-youngest.html');
const OUTPUT = path.join(ROOT, 'storybooks', 'booklist.js');

const CARD_RE = /<a\s+class="book"[^>]*href="storybooks\/(\d{2}-[^"]+)\.html"[^>]*>([\s\S]*?)<\/a>/g;
const TITLE_RE = /<h3>([\s\S]*?)<\/h3>/;

function textOf(html) {
  return html.replace(/<[^>]*>/g, '').replace(/\s+/g, ' ').trim();
}

function collect() {
  const html = fs.readFileSync(SOURCE, 'utf8');
  const books = [];
  const seen = new Set();
  let m;
  while ((m = CARD_RE.exec(html)) !== null) {
    const id = m[1];
    if (seen.has(id)) continue;
    seen.add(id);
    const title = TITLE_RE.exec(m[2]);
    books.push({
      id,
      title: title ? textOf(title[1]) : id,
      href: `/storybooks/${id}.html`
    });
  }
  return books;
}

function write(books) {
  const lines = books.map(
    (b) => `  { id: '${b.id}', title: ${JSON.stringify(b.title)}, href: '${b.href}' }`
  );
  const out =
    '/* AUTO-GENERATED by scripts/gen-booklist.js — do not edit by hand.\n' +
    '   Source of truth: card order in for-youngest.html.\n' +
    '   Regenerated by scripts/sync-gate.js. */\n' +
    'window.FO_BOOKLIST = [\n' +
    lines.join(',\n') +
    '\n];\n';
  fs.writeFileSync(OUTPUT, out);
  return out;
}

const books = collect();
if (!books.length) {
  console.error('gen-booklist: no storybook cards found in for-youngest.html');
  process.exit(1);
}
write(books);
console.log(`gen-booklist: ${books.length} books → storybooks/booklist.js`);
